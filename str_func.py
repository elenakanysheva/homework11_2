def up_str(x):
    """Принимает на вход строку
    и возвращает ее со всеми заглавными буквами"""
    return x.upper()


def title_str(x):
    """Делает заглавными первые буквы каждого слова в строке, 
    поступившей на вход"""
    return x.title()


def low_str(x):
    """Делает в строке все буквы строчными"""
    return x.lower()

